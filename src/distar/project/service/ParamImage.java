package distar.project.service;

public class ParamImage {
	private String path;
	private String sliderDir;
	private String header;
	private String bg;
	private String stuDir;
	private String empDir;
	private String behaviorDir;
	private String votingDir;
	
	

	public String getVotingDir() {
		return votingDir;
	}

	public void setVotingDir(String votingDir) {
		this.votingDir = votingDir;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getSliderDir() {
		return sliderDir;
	}

	public void setSliderDir(String sliderDir) {
		this.sliderDir = sliderDir;
	}

	public String getHeader() {
		return header;
	}

	public void setHeader(String header) {
		this.header = header;
	}

	public String getBg() {
		return bg;
	}

	public void setBg(String bg) {
		this.bg = bg;
	}

	public String getStuDir() {
		return stuDir;
	}

	public void setStuDir(String stuDir) {
		this.stuDir = stuDir;
	}

	public String getEmpDir() {
		return empDir;
	}

	public void setEmpDir(String empDir) {
		this.empDir = empDir;
	}

	public String getBehaviorDir() {
		return behaviorDir;
	}

	public void setBehaviorDir(String behaviorDir) {
		this.behaviorDir = behaviorDir;
	}
	
	

}
